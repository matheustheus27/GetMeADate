    public void exibeUser(){//Busca as informações do usuario ao qual o ID pertence e exibe
        Person user = this.dates.searchUser(jTextField1.getText());
        
        if(user != null){
            jTextArea1.setText("");
            jTextField2.setText(user.getNome());
            jTextField4.setText(user.getTelefone());
            jComboBox1.getModel().setSelectedItem(user.getSexo());
            jComboBox2.getModel().setSelectedItem(user.getSexoDePreferencia());
            jComboBox3.getModel().setSelectedItem(user.getEscolaridade());
            jComboBox4.getModel().setSelectedItem(user.getLocalizacao().getPais());
            jComboBox5.getModel().setSelectedItem(user.getLocalizacao().getEstado());
            jComboBox6.getModel().setSelectedItem(user.getLocalizacao().getCidade());
            jComboBox7.getModel().setSelectedItem(user.getIdade());
            
            for(int i = 0; i < this.proximidades.length; i++){
                if(this.proximidades[i].getText().contains(user.getLocalizacao().getProximidade())){
                    this.proximidades[i].setSelected(true);
                }
            }
            
            for(int i = 0; i < this.hobbies.length; i++){
                this.hobbies[i].setSelected(false);
                for(int j = 0; j < user.getHobbies().size(); j++){
                    if(this.hobbies[i].getText().equals(user.getHobbies().get(j))){
                        this.hobbies[i].setSelected(true);
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "As informações não foram encontradas", "Desculpe!", JOptionPane.ERROR_MESSAGE);
        }
    }
    public ArrayList<String> defineHobbies(){//Verifica os campos CheckBox e adiciona o texto das que estão selecionadas e retorna como ArrayList 
        ArrayList<String> listaHobbies = new ArrayList<>();
        
        for(int i = 0; i < this.hobbies.length; i++){
            if(this.hobbies[i].isSelected()){
                listaHobbies.add(this.hobbies[i].getText());
            }  
        }
        
        return listaHobbies;
    }
    public String defineProximidade(){//Verifica o RadioButton selecionado e retorna seu texto;
        String proximidade = "";
        for(int i = 0; i < this.proximidades.length; i++){
            if(this.proximidades[i].isSelected()){
                proximidade = this.proximidades[i].getText();
            }
        }
        return proximidade;
    }
    public void geraID(){//Gera de Forma automatica uma ID unica;
        String id;
        Random rand = new Random();
        char[] letras = "ABCDEFGHIJKLMNOPQRSTUVWXYZ".toCharArray();
        
        if(this.dates.getListaDeUsuarios().size() < 10){
            id = "GMDP" + rand.nextInt(99) + letras[rand.nextInt(letras.length)] + letras[rand.nextInt(letras.length)] + "0" + (this.dates.getListaDeUsuarios().size()+1);
        }else{
            id = "GMDP" + rand.nextInt(99) + letras[rand.nextInt(letras.length)] + letras[rand.nextInt(letras.length)] + (this.dates.getListaDeUsuarios().size()+1);
        }
        for(int i = 0; i < this.dates.getListaDeUsuarios().size(); i++){
            if(this.dates.getListaDeUsuarios().get(i).getId() == id){
                geraID();
             }
        }
        
        jTextField1.setText(id);
    }
