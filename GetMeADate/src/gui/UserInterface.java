    public void inserirUser(){
        
        //Verifica se as informações preenchidas satisfazem os requisitos
        if(jTextField1.getText() != "" && this.dates.searchUser(jTextField1.getText()) == null && jTextField2.getText() != "" && jTextField4.getText() != "" && defineHobbies().size() > 3){
            this.dates.getListaDeUsuarios().add(geraUser());//Envia os dados para serem inceridos na lista de usuarios
            JOptionPane.showMessageDialog(this, "Dados Cadastrados com Sucesso.", "Cadastro efetuado!", JOptionPane.CLOSED_OPTION);
        }else{
            JOptionPane.showMessageDialog(this, "Possiveis problemas:\n-Algum campo está vazio.\n-Você selecionou menos 4 Hobbies.\n-A ID já existe no nosso sistema (Por favor, reinicie o programa e não altere o valor padrão).", "Cadastro não efetuado!", JOptionPane.ERROR_MESSAGE);
        }
    }
